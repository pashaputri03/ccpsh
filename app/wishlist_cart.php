      $name = $_POST['name'];
      $name = strip_tags($name);
      $price = $_POST['price'];
      $price = strip_tags($price);
      $image = $_POST['image'];
      $image = strip_tags($image);
      $qty = $_POST['qty'];
      $qty = strip_tags($qty);

      $check_cart_numbers = $conn->prepare("SELECT * FROM `cart` WHERE name = ? AND user_id = ?");
